#include "types.h"

//-----------------------------------------------------------------------------
// copy len bytes from source to dest
//-----------------------------------------------------------------------------

void mymemcpy(uint8 *d, uint8 *s, uint32 len)
{
    while (len--) {
        *d++ = *s++;
    }
}

//-----------------------------------------------------------------------------
// set len bytes of dest to source byte value
//-----------------------------------------------------------------------------

void mymemset(uint8 *d, uint8 s, uint32 len)
{
    while (len--) {
        *d++ = s;
    }
}

//-----------------------------------------------------------------------------
// compare len bytes of source1 and source2
//-----------------------------------------------------------------------------

int mymemcmp(uint8 *s1, uint8 *s2, uint32 len)
{
    while (len--) {
        if (*s1++ != *s2++) {
            break;
        }
    }
    if (len == -1) {
        return (0);	// matched
    } else {
        return (1);	// didn't match
    }
}


